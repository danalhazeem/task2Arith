class Arithmetics {
    public static void main(String[] args) {
        int amountKWD = 86;
        double kuwaitiDinarToUSDollars =  amountKWD * 3.25;
        System.out.println(amountKWD+" KWD = "+kuwaitiDinarToUSDollars+" USD");

        double amountUs = 279.5 ;
        double USDollarsToKuwaitiDinar =  amountUs / 3.25;
        System.out.println(amountUs+" USD = "+USDollarsToKuwaitiDinar+" KWD");

        int amountEUR = 1000 ;
        double EURToKuwaitiDinar =  amountEUR * 0.332;
        System.out.println(amountEUR+" EUR = "+EURToKuwaitiDinar+" KWD");
    }
}